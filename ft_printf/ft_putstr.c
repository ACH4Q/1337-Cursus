#include "ft_printf.h"

int ft_putstr(const char *s)
{
    int i;
    i = 0;
    
    if(s == NULL)
    {
        write(1,"(null)",6);
        return(6);
    }
    while(*s)
    {
        ft_putchar(*s);
        i++;
        *s++;
    }
    return(i);
}