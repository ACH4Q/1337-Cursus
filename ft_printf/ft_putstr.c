#include "ft_printf.h"

int ft_putstr(const char *s)
{
    int i;
    i = 0;
    
    if (s == NULL)
    {
        ft_putstr("(null)");
        return(6);
    }
    while (*s)
    {
        ft_putchar(*s++);
        i++;
    }
    return(i);
}