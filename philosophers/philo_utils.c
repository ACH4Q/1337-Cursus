#include "philo.h"

int	ft_atoi(char *str)
{
	int (i), (sign), (result);
	(1) && (i = 0), (sign = 1), (result = 0);
	while (str[i] == ' ' || str[i] == '\t')
		i++;
	if (str[i] == '+' || str[i] == '-')
	{
		if (str[i] == '-')
			sign = -1;
		i++;
	}
	while (str[i] >= '0' && str[i] <= '9')
	{
		result = result * 10 + str[i] - '0';
		i++;
	}
	if (str[i] != '\0')
		return (-1337);
	return (sign * result);
}
